uint32_t last_reported_layer_state;
uint32_t last_reported_layer_time;

// Runs just one time when the keyboard initializes.
void * matrix_init_user(void) {
    last_reported_layer_state = 0xFFFFFFFF;
    last_reported_layer_time = timer_read32();
    return NULL;
};

// Runs constantly in the background, in a loop.
void * matrix_scan_user(void) {

    uint8_t layer = biton32(layer_state);

    ergodox_board_led_off();
    ergodox_right_led_1_off();
    ergodox_right_led_2_off();
    ergodox_right_led_3_off();
    switch (layer) {
      // TODO: Make this relevant to the ErgoDox EZ.
        case 1:
            ergodox_right_led_1_on();
            break;
        case 2:
            ergodox_right_led_2_on();
            break;
        default:
            // none
            break;
    }

    if (last_reported_layer_state != layer_state ||
        timer_elapsed32(last_reported_layer_time) > 2000)
    {
        last_reported_layer_state = layer_state;
        last_reported_layer_time = timer_read32();
        
        print_val_hex32(layer_state);
    }
    
    return NULL;

};
